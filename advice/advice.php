    <h2>Farm input and implement</h2>
    <p>Irrigation water and inputs such as seeds, fertilizers, labor and pesticides are natural 
      determinants of crop production. Balanced application of these inputs can only ensure high yield of a crop at the farm level. 
      As seed is a basic input research and extension activities are carried out for production and supply of high quality seeds of 
      high yielding varieties of crops. To date, a significant number of high-yielding varieties of rice, wheat, maize, jute, sugarcane, 
      pulses and oilseeds, tuber crops and vegetables have been released for production and on-farm use. Farmers usually save their own seed. Earlier, the government agency Bangladesh Agricultural Development Corporation supplied only certified seeds, but almost never supplied more than five percent of the total demand. 
      Currently, many privately owned companies are engaged in seed business.</p>
