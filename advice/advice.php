    <h2>Pest</h2>
    <p>Any organism including pests, mites, nematodes, rodents or birds that attack crops and cause economic damage. 
      According to current estimates, the annual crop loss due to insects alone in Bangladesh is 16% of rice, 15% of jute, 11% of wheat, 
      20% of sugarcane, 25% of vegetables and 25% of pulses. Apart from various species of grain-eating birds and rodents, 
      a total of 700 species of insects and mites infesting stored grain and goods have been listed in the country so far, 
      of which more than two hundred species are considered as 'major' pests.
       The status of a pest as 'major' or 'minor' usually depends on the conditions prevailing in a particular ecosystem.</p>
